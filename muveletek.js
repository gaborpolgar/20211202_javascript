function osszead(a, b) {
    a = parseFloat(a);
    b = parseFloat(b);
    return a + b;
}
function kivon(c, d){
    c = parseFloat(c);
    d = parseFloat(d);
    return c - d;
}
function szoroz(e, f){
    e=parseFloat(e);
    f=parseFloat(f);
    return e*f;
}
function oszt(g, h){
    g=parseFloat(g);
    h=parseFloat(h);
    return g/h;
}